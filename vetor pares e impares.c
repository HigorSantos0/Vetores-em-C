//importação de bibliotecas
#include <stdio.h>

//definição de constantes
#define TAM 300

//protótipos das funções
void questao05 (int vetor[], int tamanho, int vetPar[], int *tamPar, int vetImpar[], int *tamImpar);

void exibir (int vetor[], int tamanho);
void preencherAleatorio (int vetor[], int tamanho);

//main
void main ()
{
	//declaração de variáveis
	int v[TAM], vetP[TAM], vetI[TAM];
	int quantP, quantI;
	
	//preenchendo o vetor aleatoriamente
	preencherAleatorio (v, TAM);

	//exibindo o vetor
	exibir (v, TAM);
	
	//chamando a função 
	questao05 (v, TAM, vetP, &quantP, vetI, &quantI);
	
	//exibindo o vetor de pares
	printf ("\nPares (%d elementos):\n", quantP);
	exibir (vetP, quantP);
	
	//exibindo o vetor de ímpares
	printf ("\nImpares (%d elementos):\n", quantI);
	exibir (vetI, quantI);
}

//implementação das funções
void questao05 (int vetor[], int tamanho, int vetPar[], int *tamPar, int vetImpar[], int *tamImpar)
{
	//declaração de variáveis
	int i, j = 0, k = 0;
	
	//varrendo o vetor original
	for (i=0;i<tamanho;i++)	
	{
		//verificando se o elemento da posição 'i' é par ou ímpar
		if (vetor[i] % 2 == 0)
		{
			vetPar[j] = vetor[i];
			j++;
		}
		else
		{
			vetImpar[k] = vetor[i];
			k++;
		}
	}
	
	//armazenando nos parâmetros tamPar e tamImpar a quantidade de pares e impares, respectivamente
	*tamPar = j;
	*tamImpar = k;
}

void exibir (int vetor[], int tamanho)
{
	//declaração de variáveis
	int i;
	
	printf ("\n\nElementos do vetor: ");
	
	//varrendo o vetor
	for (i=0;i<tamanho;i++)	
	{
		printf ("%d ", vetor[i]);
	}
	
	printf ("\n\n");
}

void preencherAleatorio (int vetor[], int tamanho)
{
	//declaração de variáveis
	int i;
	
	//garantindo que as sequências serão distintas a cada execução
	srand (time(NULL));

	//varrendo o vetor
	for (i=0;i<tamanho;i++)	
	{
		vetor[i] = rand ()%30;
	}	
}
